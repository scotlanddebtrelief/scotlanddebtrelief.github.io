  to track the online movements of Web users. In contrast to cookies,   
which are stored on a user’s computer hard drive, clear gifs are   
embedded invisibly on Web pages and are about the size of the period at 
  the end of this sentence. We do not tie the information gathered by   
clear gifs to our customers’ personally identifiable information. We use
   clear gifs in our HTML-based emails to let us know which emails have 
  been opened by recipients. This allows us to gauge the effectiveness 
of   certain communications and the effectiveness of our marketing 
campaigns.   If you would like to opt-out of these emails, please see 
"Choice and   Opt-out."</p>
